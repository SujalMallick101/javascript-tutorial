//while loop
let index=0
while (index<=10) {
    console.log(`value of index : ${index}`);
    index++
}

//while loop using array
let array=["flash","thor","batman"]
let arr=0
while(arr<array.length){
    console.log(array[arr]);
    arr++;
}

//do-while loop
let score=1
do {
    console.log(`score is : ${score}`);
    score++;
} while (score<=10);
