console.log(2>1)//true
console.log(2>=1);//true
console.log(2<1);//false

//Not a good practice if data types are different
console.log("2">1);//true
console.log("02">1);//true


//Not predictable
console.log(null>0);//false
console.log(null == 0);//false
console.log(null >= 0);//true


//strict check
console.log("2" === 2);//false





